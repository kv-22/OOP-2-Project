    private void jButtonBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBookActionPerformed
        // TODO add your handling code here:
        try {
            con = MyConnection.connectdb();
            String q = "INSERT INTO APPOINTMENTS (DATE,TIME,CUSTOMERNAME,CUSTOMER_IDCUSTOMER,S_ID) VALUES (?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(q);
            pst.setDate(1, Date.valueOf(date));
            pst.setString(2, jComboBoxTime.getSelectedItem().toString());
            pst.setString(3, customer.getName());
//            System.out.println(findCustomerID);
            pst.setInt(4, Integer.parseInt(customer.getUserID()));
            pst.setInt(5, Integer.parseInt(jTextFieldID.getText()));
            int result = pst.executeUpdate();
            if (result == 1) {
                JOptionPane.showMessageDialog(null, "Appointment has been booked successfully.");
                this.dispose();
                new CustomerMenu(customer).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error booking appointment.");
            }

            pst.close();
            closeDb();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please check all fields.");
        }

    }//GEN-LAST:event_jButtonBookActionPerformed

