    private void jRadioButtonGroomingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jRadioButtonGroomingMouseClicked
        // TODO add your handling code here:
        if (jTextFieldID.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Please enter service ID.");
            buttonGroup1.clearSelection();

            return;
        }

        try {
            con = MyConnection.connectdb();
            st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            rs = st.executeQuery("SELECT PACKAGE FROM GROOMINGSERVICES, SERVICES WHERE IDSERVICE= " + Integer.valueOf(jTextFieldID.getText()) + " AND SID = IDSERVICE");
            jComboBoxPackage.removeAllItems();
            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(null, "Please enter correct grooming service ID.");
                buttonGroup1.clearSelection();
                return;
            }
            jComboBoxPackage.setEnabled(true);
            while (rs.next()) {
                jComboBoxPackage.addItem(rs.getString(1));

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "CHECK ID.");
        }
        closeDb();

    }//GEN-LAST:event_jRadioButtonGroomingMouseClicked

